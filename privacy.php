      1) Remember the items in your shopping basket


      2) Recognize you when you return to our website


      3) Analyze visitor behavior you can choose to have your computer warn you each time a cookie is being sent or you can choose to turn off all cookies. You do this through your browser settings. Please note, however, that by deleting our cookies or disabling future cookies you may not be able to access certain areas or features of our website.


